#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*bzero;

	i = 0;
	bzero = s;
	while (i < n)
	{
		if (bzero[i] == '\0')
			i++;
		else
		{
			bzero[i] = '\0';
			i++;
		}
	}
}

#include "libft.h"

void		ft_memdel(void **ap)
{
	int i;

	i = 0;
	if (ap)
	{
		while (*ap)
		{
			free(ap[i]);
			ap[i] = NULL;
			i++;
		}
		ap = NULL;
	}
}
